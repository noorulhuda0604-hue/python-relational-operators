# Relational operator 
a = 10
b = 5
print(a > b)    # True
print(a < b)    # False
print(a == b)   # False
print(a != b)   # True
print(a >= b)   # True
print(a <= b)   # False
# user input 
num1 = int(input("First number: "))
num2 = int(input("Second number: "))

print("Greater:", num1 > num2)
print("Equal:", num1 == num2)
print("Not Equal:", num1 != num2)